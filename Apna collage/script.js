//arithmatic operator
let a = 5;
let b = 6;
console.log("a= ", a, "& b = ", b);
console.log("a-b = ", a - b);
console.log("a*b = ", a * b);
console.log("a/b = ", a / b);
console.log("a%b = ", a % b);
console.log("a**b = ", a ** b);

//unary operator
let c = 4;
let d = 7;
console.log("c= ", c, "& d = ", d);
console.log("c++ =", c++);
console.log("c =", c);
console.log("c= ", c, "& d = ", d);
console.log("++c =", ++c);
console.log("c =", c);

console.log("c= ", c, "& d = ", d);
console.log("c-- =", c--);
console.log("c =", c);
console.log("c= ", c, "& d = ", d);
console.log("--c =", --c);
console.log("c =", c);


